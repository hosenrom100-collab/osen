export function getHelpForPath(pathname: string): PageHelp {
  if (pathname.includes("/shopping")) {
    return helpContent.shopping;
  }
  if (pathname.includes("/admin")) {
    return helpContent.admin;
  }
  if (pathname.includes("/patients/")) {
    return helpContent.patientDetail;
  }
  if (pathname.includes("/patients")) {
    return helpContent.patients;
  }
  if (pathname.includes("/attendance")) {
    return helpContent.attendance;
  }
  return helpContent.dashboard;
}
