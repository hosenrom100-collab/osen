function fillName(text: string, name: string): string {
  return text.replace(/{name}/g, name);
}

function getHashCode(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  return Math.abs(hash);
}

function pickVariant(variants: string[], patientName: string): string {
  if (!variants || variants.length === 0) return "";
  const index = getHashCode(patientName) % variants.length;
  return variants[index];
}

export function composeParagraph(fragments: string[], patientName: string = ""): string {
  const validFragments = fragments
    .filter((f): f is string => Boolean(f && f.trim()))
    .map((f) => f.trim());

  if (validFragments.length === 0) return "";

  const connectors = [
    "כמו כן, ",
    "בנוסף, ",
    "ראוי לציין כי ",
    "במקביל, ",
    "לצד זאת, ",
    "יתרה מכך, ",
    "בהקשר זה, "
  ];

  let result = "";
  validFragments.forEach((f, idx) => {
    const cleanSentence = f.replace(/[.!?]+$/, "");
    if (idx === 0) {
      result = cleanSentence;
    } else {
      // Check if sentence already starts with a connective word in Hebrew
      const hasConnector = /^(בנוסף|כמו כן|לצד זאת|על אף|למרות|מנגד|עם זאת|יחד עם זאת|ראוי לציין|בהקשר זה|בעקבות|במקביל|יתרה מכך)/.test(cleanSentence);
      if (hasConnector) {
        result += ". " + cleanSentence;
      } else {
        const hash = patientName ? getHashCode(patientName) : 0;
        const conn = connectors[(hash + idx) % connectors.length];
        result += ". " + conn + cleanSentence;
      }
    }
  });

  if (result && !/[.!?]$/.test(result)) {
    result += ".";
  }

  return result;
}

